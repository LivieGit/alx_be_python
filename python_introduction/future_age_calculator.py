# Future age calculator

# Get user's current age
current_age = int(input("How old are you? "))

# Target year
target_year = 2050
# Assuming current year is 2023

# Calculate age in target year
age_in_target_year = current_age + (target_year - 2023)

# Print the result
print(f"In {target_year}, you will be {age_in_target_year} years old.")
